int hex_string_to_int( const char *hex_string )
{
    int result = 0;
    int length = 2; //strlen( hex_string );
    for( int i = 0; i < length; i++ ) {
        char c = hex_string[i];
        int  value = 0;
        if( c >= '0' && c <= '9' ) {
            value = c - '0';
        }
        else if( c >= 'a' && c <= 'f' ) {
            value = c - 'a' + 10;
        }
        else if( c >= 'A' && c <= 'F' ) {
            value = c - 'A' + 10;
        }
        else {
            printf( "unrecognized hex string %c\n", c );
        }
        result = ( result * 16 ) + value;
    }
    return result;
}

int strip_record( uint8_t *rtn_rec, int cur_index, int *read_len )
{
    int start_pos = cur_index;
    // Find the record start
    while( prg_str[start_pos++] != ':' ) {
        printf( "invalid start string %c\n", prg_str[start_pos - 1] );
    }

    // Get the byte count
    int count = hex_string_to_int( &prg_str[start_pos] );
    start_pos += 2;
    *read_len = count;

    // Get the address
    int addr = hex_string_to_int( &prg_str[start_pos] );
    addr <<= 8;
    start_pos += 2;
    addr |= hex_string_to_int( &prg_str[start_pos] );
    start_pos += 2;

    // Get the record type
    int type = hex_string_to_int( &prg_str[start_pos] );
    start_pos += 2;

    // Get the record
    for( int i = 0; i < count; i++ ) {
        rtn_rec[i] = hex_string_to_int( &prg_str[start_pos] );
        start_pos += 2;
    }

    // Get the checksum
    int check_sum = hex_string_to_int( &prg_str[start_pos] );
    start_pos += 2;

    // Print the record
    printf( "hex record len: %x, addr %x, type %x, checksum %x\n", count, addr, type, check_sum );
    printf( "should've read: " );
    for( int i = cur_index; i < start_pos; i++ ) printf( "%c", prg_str[i] );
    printf( "\n" );
    printf( "read:" );
    for( int i = 0; i < count; i++ ) printf( "%X", rtn_rec[i] );
    printf( "\n" );

    // Return the number of bytes read from the buffer
    return start_pos - cur_index;
}

